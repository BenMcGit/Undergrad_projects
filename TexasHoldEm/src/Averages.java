
public class Averages {
	public double wins;
	public double lose1;
	public double lose2;

	public Averages(double wins, double lose1, double lose2){
		this.wins = wins;
		this.lose1 = lose1;
		this.lose2 = lose2;
	}

	public double getWins() {
		return wins;
	}

	public void setWins1(double wins) {
		this.wins = wins;
	}

	public double getLose1() {
		return lose1;
	}

	public void setLose1(double lose1) {
		this.lose1 = lose1;
	}

	public double getLose2() {
		return lose2;
	}

	public void setLose2(double lose2) {
		this.lose2 = lose2;
	}
}
